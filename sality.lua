p_sality = Proto("sality","Sality P2P Protocol")

local types = { [1] = "Hello", [2] = "Peer Exchange", [3] = "URL Pack Exchange" }

local f = p_sality.fields
f.crc16 = ProtoField.uint16("sality.crc16", "CRC16 Checksum", base.HEX)
f.msglen = ProtoField.uint16("sality.msglen", "Message Length")
f.rc4key = ProtoField.bytes("sality.rc4key", "RC4 Key")
f.encrypted = ProtoField.bytes("sality.encrypted", "Encrypted Data")

f.decrypted = ProtoField.bytes("sality.decrypted", "Decrypted Data")

f.protover = ProtoField.uint8("sality.protover", "Protocol Version")
f.urlpackversion = ProtoField.uint32("sality.urlpackversion", "URL Pack Version")
f.msgtype = ProtoField.uint8("sality.msgtype", "Message Type")
f.payload = ProtoField.bytes("sality.payload", "Payload")

f.botinfo = ProtoField.bytes("sality.peer", "Bot Information")
f.botinfoid = ProtoField.uint32("sality.peerid", "ID")
f.botinfoport = ProtoField.uint16("sality.peerport", "Port")

f.peer = ProtoField.bytes("sality.peer", "Peer")
f.peeripaddr = ProtoField.ipv4("sality.peeripaddr", "IP Address")
f.peerport = ProtoField.uint16("sality.peerport", "Port")
f.peerid = ProtoField.uint32("sality.peerid", "ID")

f.urlpack = ProtoField.bytes("sality.urlpack", "URL Pack")
f.urlpackstatus = ProtoField.bytes("sality.urlpackstatus", "Status")
f.urlpackmarker = ProtoField.uint32("sality.urlpackmarker", "Marker", base.HEX)
f.urlpacksig = ProtoField.bytes("sality.urlpacksig", "Signature")
f.urlpackver = ProtoField.uint32("sality.urlpackver", "Version")
f.urlpackunknown2 = ProtoField.uint32("sality.urlpackunknown2", "Unknown", base.HEX)
f.urlpackunknown3 = ProtoField.uint32("sality.urlpackunknown3", "Unknown", base.HEX)
f.urlpackentries = ProtoField.uint16("sality.urlpackentries", "Number of URLs")
f.urlpackurl = ProtoField.string("sality.urlpackurl", "URL")

f_msglen = Field.new("sality.msglen")
f_rc4key = Field.new("sality.rc4key")
f_peeripaddr = Field.new("sality.peeripaddr")
f_peerport = Field.new("sality.peerport")
f_peerid = Field.new("sality.peerid")


local function rc4(key, input)
  local s = {}
  for i = 0, 255 do
    s[i] = i
  end

  local j = 0
  for i = 0, 255 do
    j = (j + s[i] + key:byte(i % #key + 1)) % 256
    s[i], s[j] = s[j], s[i]
  end

  local output = ''
  local x, y = 0, 0

  for i = 0, #input-1 do
    x = (x + 1) % 256
    y = (y + s[x]) % 256

    s[x], s[y] = s[y], s[x]
    j = s[(s[x] + s[y]) % 256]
    output = output .. string.format("%02x", bit.bxor(input:byte(i + 1), j))
    end

    return output
end


local function htons(n)
  return bit.band(0xffff, bit.bor(bit.lshift(n, 8), bit.rshift(n, 8)))
end


function p_sality.dissector (buf, pkt, root)
  if buf:len() == 0 or buf:len() > 4096 then return end

  pkt.cols.protocol:set("Sality")
 
  subtree = root:add(p_sality, buf)

  subtree:add_le(f.crc16, buf(0,2))
  subtree:add_le(f.msglen, buf(2,2))
  subtree:add(f.rc4key, buf(0,4))

  encrypted = subtree:add(f.encrypted, buf(4, f_msglen().value))
  
  local key = buf:raw(0, 4)
  local enc = buf:raw(4, f_msglen().value)
  local dec = rc4(key, enc)
  local decbuf = ByteArray.new(dec)
  local dectvb = decbuf:tvb('decrypted')

  encrypted:add(f.decrypted, buf(4, f_msglen().value), dectvb:raw(0, f_msglen().value))

  encrypted:add(f.protover, buf(4,1), dectvb(0,1):uint())
  encrypted:add(f.urlpackversion, buf(5,4), bit.bswap(dectvb(1,4):uint()))
  encrypted:add(f.msgtype, buf(9,1), dectvb(5,1):uint())

  local payloadlen = f_msglen().value - 6

  pkt.cols.info:set(types[dectvb(5,1):uint()])

  if payloadlen > 0 then
    if dectvb(5,1):uint() == 1 then
      if payloadlen >= 6 then
          botinfo = encrypted:add(f.botinfo, buf(10,payloadlen), dectvb:raw(6,payloadlen))
          botinfo:add(f.botinfoid, buf(10,4), bit.bswap(dectvb(6,4):uint()))
          botinfo:add(f.botinfoport, buf(14,2), htons(dectvb(10,2):uint()))

          if bit.bswap(dectvb(6,4):uint()) == 1 then
            pkt.cols.info:set(types[dectvb(5,1):uint()] .. ", port " .. htons(dectvb(10,2):uint()) .. ", id " .. bit.bswap(dectvb(6,4):uint()) .. " (request)")
          elseif bit.bswap(dectvb(6,4):uint()) < 16000000 then
            pkt.cols.info:set(types[dectvb(5,1):uint()] .. ", port " .. htons(dectvb(10,2):uint()) .. ", id " .. bit.bswap(dectvb(6,4):uint()) .. " (natted)")
          elseif bit.bswap(dectvb(6,4):uint()) >= 16000000 and htons(dectvb(10,2):uint()) <= 20000000 then
            pkt.cols.info:set(types[dectvb(5,1):uint()] .. ", port " .. htons(dectvb(10,2):uint()) .. ", id " .. bit.bswap(dectvb(6,4):uint()) .. " (public)")
          else
            pkt.cols.info:set(types[dectvb(5,1):uint()] .. ", port " .. htons(dectvb(10,2):uint()) .. ", id " .. bit.bswap(dectvb(6,4):uint()) .. " (invalid)")
          end
      end
    end

    if dectvb(5,1):uint() == 2 then
      if payloadlen >= 10 then
        if dectvb(6,4):uint() ~= 0 and dectvb(10,2):uint() ~= 0 and dectvb(12,4):uint() ~= 0 then
          peer = encrypted:add(f.peer, buf(10,20), dectvb:raw(6,10))
          peer:add(f.peeripaddr, buf(10,4), dectvb(6,4):ipv4())
          peer:add(f.peerport, buf(14,2), htons(dectvb(10,2):uint()))
          peer:add(f.peerid, buf(16,4), bit.bswap(dectvb(12,4):uint()))

          if bit.bswap(dectvb(12,4):uint()) >= 16000000 and bit.bswap(dectvb(12,4):uint()) <= 20000000 then
            pkt.cols.info:set(types[dectvb(5,1):uint()] .. ", " .. tostring(dectvb(6,4):ipv4()) .. ":" .. htons(dectvb(10,2):uint()) .. " " .. bit.bswap(dectvb(12,4):uint()))
          else
            pkt.cols.info:set(types[dectvb(5,1):uint()] .. ", " .. tostring(dectvb(6,4):ipv4()) .. ":" .. htons(dectvb(10,2):uint()) .. " " .. bit.bswap(dectvb(12,4):uint()) .. " (invalid id)")
          end
        end
      end
    end
  end

  if dectvb(5,1):uint() == 3 then
      if payloadlen >= 154 then
          urlpack = encrypted:add(f.urlpack, buf(10,payloadlen), dectvb:raw(6,payloadlen))
          urlpack:add(f.urlpackstatus, buf(10,2), dectvb(6,2):string())
          urlpack:add(f.urlpackmarker, buf(12,4), dectvb(8,4):uint())
          urlpack:add(f.urlpacksig, buf(16,128), dectvb:raw(12,128))
          urlpack:add(f.urlpackver, buf(144,4), bit.bswap(dectvb(140,4):uint()))
          urlpack:add(f.urlpackunknown2, buf(148,4), dectvb(144,4):uint())
          urlpack:add(f.urlpackunknown3, buf(152,4), dectvb(148,4):uint())
          urlpack:add(f.urlpackentries, buf(156,2), htons(dectvb(152,2):uint()))

          local offset = 154
          for i = 1,htons(dectvb(152,2):uint()) do
	    local url = dectvb(offset):stringz()
	    urlpack:add(f.urlpackurl, buf(offset + 4,#url+1), dectvb:raw(offset,0)):append_text(url)
            offset = offset + #url + 1
          end

          pkt.cols.info:set(types[dectvb(5,1):uint()] .. ", version " .. bit.bswap(dectvb(140,4):uint()) .. " (" .. htons(dectvb(152,2):uint()) .. " URLs)")
      end
  end

  pkt.cols.protocol:set("Sality")
 
end
 
